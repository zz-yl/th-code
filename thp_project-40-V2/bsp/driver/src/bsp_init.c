/**
* @brief  bsp初始化
* @attention 
*/
void bsp_init(void)
{
    sys_cache_enable();      /* 打开L1-Cache */
    HAL_Init();              /* 初始化HAL库 */
    system_clock_config();   /* 设置时钟, 480Mhz */
    mpu_memory_protection(); /* 保护相关存储区域 */
    bsp_io_init();             //IO初始化
    tim6_init(1000-1, 240-1);  //定时器初始化

    /* 串口初始化 */
    uart3_init();
    uart4_init();
    uart5_init();
    uart6_init();
    uart7_init();
    uart8_init();
    i2c3_init();
}
