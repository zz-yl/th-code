/**
* @brief  bsp初始化
* @attention 
*/
void bsp_init(void)
{
    HAL_Init();              /* 初始化HAL库 */
    system_clock_config();   /* 设置时钟, 80Mhz */
    
    bsp_io_init();
    bsp_tim1_pwm_init(VALVE_MAX-1, 1-1);
    uart1_init();
    uart2_init();
    uart3_init();
    i2c1_init();
//    i2c2_init();
//    i2c3_init();
    bsp_i2cs_init();
//    spi2_init();
}
