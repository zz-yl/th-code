/**
* @brief  内联函数,数据边界限制,限制data的上下限
*/
float Limit(float data, float max, float min);
__inline float Limit(float data, float max, float min)
{
    if(data > max)
    {
        data = max;
    }
    if(data < min)
    {
        data = min;
    }
    return data;
}

