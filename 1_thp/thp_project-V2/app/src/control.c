/**
* @brief  灯光控制
*/
void ctrl_led(uint16_t color)
{
    switch(color)
    {
        case CTRL_RED   : IO_LED_R(1); IO_LED_G(0); IO_LED_B(0); break;
        case CTRL_GREEN : IO_LED_R(0); IO_LED_G(1); IO_LED_B(0); break;
        case CTRL_BLUE  : IO_LED_R(0); IO_LED_G(0); IO_LED_B(1); break;
        case CTRL_YELLOW: IO_LED_R(1); IO_LED_G(1); IO_LED_B(0); break;
        case CTRL_PURPLE: IO_LED_R(1); IO_LED_G(0); IO_LED_B(1); break;
        case CTRL_CYAN  : IO_LED_R(0); IO_LED_G(1); IO_LED_B(1); break;
        case CTRL_WHITE : IO_LED_R(1); IO_LED_G(1); IO_LED_B(1); break;
        default:          IO_LED_R(0); IO_LED_G(0); IO_LED_B(0); break;
    }
}
